package com.myapplicationdev.android.p03classjournal;

public class Modules {

    private String code;
    private String name;

    public Modules(String code, String name) {
        this.code = code;
        this.name = name;
    }

    public String getCode() {
        return code;
    }


    public String getName() {
        return name;
    }
}
